# 1. Single-Line Comment
# -------------------------------
print("Hello, World!")


# -------------------------------
# 2. Multiple Single-Line Comments
# -------------------------------
# This is the first comment.
# This is the second comment.
# This is the third comment.

print("Welcome to Python!")


# -------------------------------
# 3. Inline Comment
# -------------------------------
print("Python is easy to learn.")   # This comment is written after a statement


# -------------------------------
# 4. Multi-Line String (Often Used as a Multi-Line Comment)
# -------------------------------
"""
This is a multi-line string.

Although Python treats this as a string,
it is commonly used as a multi-line comment
when it is not assigned to any variable.
"""

print("Using triple double quotes.")


# -------------------------------
# 5. Triple Single Quotes
# -------------------------------
'''
This is another way to write
a multi-line string.

Many programmers also use it
as a multi-line comment.
'''

print("Using triple single quotes.")


# -------------------------------
# 6. Docstring (Documentation Comment)
# -------------------------------
def greet():
    """
    This function prints a greeting message.

    This is called a Docstring.
    It documents what the function does.
    """
    print("Hello from the greet() function!")

greet()



# Practical use of quotes and escape sequences

# Single quotes
name = 'Mohit'

# Double quotes
city = "Jaipur"

print("===== BASIC PYTHON EXAMPLE =====")

# Using variables
print('Name:', name)
print("City:", city)

# New line (\n)
print("\nThis sentence starts on a new line.")

# Tab space (\t)
print("Name\t:\tMohit")
print("Age\t:\t29")
print("City\t:\tJaipur")

# Single quote inside a string
print("He said, 'Python is easy to learn.'")

# Double quote inside a string
print('She said, "Practice every day."')

# Escaping single quote
print('It\'s a beautiful day.')

# Escaping double quote
print("The teacher said, \"Good Job!\"")

# Backslash
print("File Location: C:\\Users\\Mohit\\Documents")

# Combining multiple escape sequences
print("\nStudent Details")
print("---------------------")
print("Name\t:\tMohit")
print("Course\t:\tPython")
print("Status\t:\t\"Learning\"")
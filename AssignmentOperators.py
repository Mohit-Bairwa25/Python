# Program to demonstrate all Assignment Operators in Python

# Assign (=)
a = 10
print("Initial Value of a =", a)

# Add and Assign (+=)
a += 5
print("After a += 5 :", a)

# Subtract and Assign (-=)
a -= 3
print("After a -= 3 :", a)

# Multiply and Assign (*=)
a *= 2
print("After a *= 2 :", a)

# Divide and Assign (/=)
a /= 4
print("After a /= 4 :", a)

# Floor Divide and Assign (//=)
a //= 2
print("After a //= 2 :", a)

# Modulus and Assign (%=)
a %= 3
print("After a %= 3 :", a)

# Exponent and Assign (**=)
a **= 2
print("After a **= 2 :", a)
# Integer
age = 25

# Float
height = 5.9

# String
name = "Mohit"

# Boolean
is_student = True

# List (ordered, changeable)
fruits = ["apple", "banana", "cherry"]

# Tuple (ordered, unchangeable)
coordinates = (10, 20)

# Dictionary (key-value pairs)
person = {"name": "Mohit", "age": 25}

# Set (unordered, unique values)
unique_numbers = {1, 2, 3, 3, 2}

# None type
result = None

# Complex number
z = 2 + 3j

# Printing all variables with their types
print("age:", age, "->", type(age))
print("height:", height, "->", type(height))
print("name:", name, "->", type(name))
print("is_student:", is_student, "->", type(is_student))
print("fruits:", fruits, "->", type(fruits))
print("coordinates:", coordinates, "->", type(coordinates))
print("person:", person, "->", type(person))
print("unique_numbers:", unique_numbers, "->", type(unique_numbers))
print("result:", result, "->", type(result))
print("z:", z, "->", type(z))
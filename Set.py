# ======================================================
#               PYTHON SET OPERATIONS
# ======================================================

# 1. Creating a Set
print("\n1. Creating a Set")

fruits = {"Apple", "Banana", "Mango", "Orange"}

print(fruits)

# ======================================================
# 2. Length
# ======================================================

print("\n2. Length")

print(len(fruits))

# ======================================================
# 3. Adding an Element
# ======================================================

print("\n3. add()")

fruits.add("Grapes")

print(fruits)

# ======================================================
# 4. Updating Set
# ======================================================

print("\n4. update()")

fruits.update(["Kiwi", "Papaya"])

print(fruits)

# ======================================================
# 5. Removing Elements
# ======================================================

print("\n5. remove()")

fruits.remove("Banana")

print(fruits)

# ======================================================
# 6. discard()
# ======================================================

print("\n6. discard()")

fruits.discard("Apple")

print(fruits)

# ======================================================
# 7. pop()
# ======================================================

print("\n7. pop()")

removed = fruits.pop()

print("Removed Element:", removed)
print(fruits)

# ======================================================
# 8. Membership Operators
# ======================================================

print("\n8. Membership Operators")

print("Mango" in fruits)
print("Banana" not in fruits)

# ======================================================
# 9. Copy
# ======================================================

print("\n9. copy()")

new_set = fruits.copy()

print(new_set)

# ======================================================
# 10. Union
# ======================================================

print("\n10. union()")

set1 = {1, 2, 3}
set2 = {3, 4, 5}

print(set1.union(set2))

# ======================================================
# 11. Intersection
# ======================================================

print("\n11. intersection()")

print(set1.intersection(set2))

# ======================================================
# 12. Difference
# ======================================================

print("\n12. difference()")

print(set1.difference(set2))

# ======================================================
# 13. Symmetric Difference
# ======================================================

print("\n13. symmetric_difference()")

print(set1.symmetric_difference(set2))

# ======================================================
# 14. issubset()
# ======================================================

print("\n14. issubset()")

A = {1, 2}
B = {1, 2, 3, 4}

print(A.issubset(B))

# ======================================================
# 15. issuperset()
# ======================================================

print("\n15. issuperset()")

print(B.issuperset(A))

# ======================================================
# 16. isdisjoint()
# ======================================================

print("\n16. isdisjoint()")

X = {1, 2}
Y = {3, 4}

print(X.isdisjoint(Y))

# ======================================================
# 17. clear()
# ======================================================

print("\n17. clear()")

temp = {10, 20, 30}

temp.clear()

print(temp)

# ======================================================
# 18. Empty Set
# ======================================================

print("\n18. Empty Set")

empty = set()

print(empty)

# ======================================================
# 19. Duplicate Elements
# ======================================================

print("\n19. Duplicate Elements")

numbers = {1, 2, 2, 3, 3, 4, 5}

print(numbers)

# ======================================================
# 20. Loop Through Set
# ======================================================

print("\n20. For Loop")

for item in numbers:
    print(item)

# ======================================================
# 21. Frozen Set
# ======================================================

print("\n21. frozenset()")

fixed = frozenset([10, 20, 30])

print(fixed)

# ======================================================
# 22. Set Properties
# ======================================================

print("\n22. Set Properties")

print("Sets are unordered.")
print("Sets do not allow duplicate values.")
print("Sets are mutable.")
print("Set elements must be immutable.")

# ======================================================
# End of Program
# ======================================================

print("\nProgram Completed Successfully!")
# Program to demonstrate all Arithmetic Operators in Python

# Input two numbers
a = 20
b = 6

print("First Number (a):", a)
print("Second Number (b):", b)

# Addition (+)
print("\nAddition (+):", a + b)

# Subtraction (-)
print("Subtraction (-):", a - b)

# Multiplication (*)
print("Multiplication (*):", a * b)

# Division (/)
print("Division (/):", a / b)

# Floor Division (//)
print("Floor Division (//):", a // b)
#The decimal part (0.333...) is removed by rounding down.

# Modulus (%)
print("Modulus (%):", a % b)

# Exponentiation (**)
print("Exponentiation (**):", a ** b)